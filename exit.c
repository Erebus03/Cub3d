#include "cub3d.h"

void	exitcub(t_cub *c)
{
	if (c->mlx)
	{
		mlx_destroy_window(c->mlx, c->win);
		mlx_destroy_display(c->mlx);
		free(c->mlx);
	}
	exit(0);
}
